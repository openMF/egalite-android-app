package com.bfsi.egalite.entity;

public class CcyCodes {
	private String ccyName;
	private String isoCcyCode;
	private String isoAltCode;
	private String country;
	private String roundRule;
	private String decimal;
	private String ccyCode;
	public String getCcyCode() {
		return ccyCode;
	}
	public void setCcyCode(String ccyCode) {
		this.ccyCode = ccyCode;
	}
	public String getCcyName() {
		return ccyName;
	}
	public void setCcyName(String ccyName) {
		this.ccyName = ccyName;
	}
	public String getIsoCcyCode() {
		return isoCcyCode;
	}
	public void setIsoCcyCode(String isoCcyCode) {
		this.isoCcyCode = isoCcyCode;
	}
	public String getIsoAltCode() {
		return isoAltCode;
	}
	public void setIsoAltCode(String isoAltCode) {
		this.isoAltCode = isoAltCode;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getRoundRule() {
		return roundRule;
	}
	public void setRoundRule(String roundRule) {
		this.roundRule = roundRule;
	}
	public String getDecimal() {
		return decimal;
	}
	public void setDecimal(String decimal) {
		this.decimal = decimal;
	}
	
}
