package com.bfsi.egalite.entity;

public class GroupLoans {
	private String acNo;
	private String customerName;
	private Double agendaAmt;
	public String getAcNo() {
		return acNo;
	}
	public void setAcNo(String acNo) {
		this.acNo = acNo;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public Double getAgendaAmt() {
		return agendaAmt;
	}
	public void setAgendaAmt(Double agendaAmt) {
		this.agendaAmt = agendaAmt;
	}
	
	
	

}
